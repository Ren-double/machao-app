const LANGUAGE_KEY = '@language_settings';

export const initI18n = async () => {
  try {
    const savedLanguage = await AsyncStorage.getItem(LANGUAGE_KEY);
    if (savedLanguage) {
      i18n.locale = savedLanguage === 'chinese' ? 'zh' : 'en';
    } else {
      // Default to device locale or 'zh'
      const deviceLanguage = getLocales()[0]?.languageCode;
      i18n.locale = deviceLanguage === 'zh' ? 'zh' : 'en';
      // If no saved language, maybe save the default?
    }
  } catch (error) {
    console.error('Failed to load language', error);
    i18n.locale = 'zh'; // Fallback
  }
  i18n.enableFallback = true;
};

// Sync function to get current locale for initial render if needed (might be stale until init)
export const getCurrentLocale = () => i18n.locale;

export const setLanguage = async (lang: 'zh' | 'en') => {
  i18n.locale = lang;
  await AsyncStorage.setItem(LANGUAGE_KEY, lang === 'zh' ? 'chinese' : 'english'); // Keep compatibility with existing key values
};

export default i18n;
